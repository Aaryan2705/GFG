class Solution {
  public:
    Node* addOne(Node* head) {
        vector<Node*> v;
        Node* temp = head;
        while(temp->next!=NULL){
            v.push_back(temp);
            temp=temp->next;
        }
        while(temp->data==9){
                if(temp==head){
                    temp->data=0;
                    Node* new_node = new Node(1);
                    new_node->next=temp;
                    head=new_node;
                    return head;
                }
                else{
                    temp->data=0;
                    temp=v.back();
                    v.pop_back();
                }
        }
        if(temp->data!=9){
            temp->data +=1;
        }
        return head;     
    }
};


